export const getPageTitle = ({ pathName, query: pQuery }) => {
  let query = pQuery;
  let currentPageName = null;
  const splittedPaths = pathName?.split("/");
  navigationConfig.map((item) => {
    if (item.type === "collapse") {
      item.children.map((sItem) => {
        if (sItem.navLink === pathName) {
          currentPageName = sItem?.pageTitle;
        }
        return sItem;
      });
    } else if (item.navLink === pathName) {
      currentPageName = item?.pageTitle;
    }
    return item;
  });
  // preventive
  if (!currentPageName && query) {
    query = query?.workOrderId ? "workOrderDetail" : query;

    if (query === "workOrderDetail") {
      currentPageName = "Work Order - Details";
    }
    if (query?.comparisonID) {
      currentPageName = "Dashboard";
    } else if (splittedPaths?.includes("work-order-create")) {
      currentPageName = "Inventories";
    }
  } else if (!currentPageName) {
    currentPageName = "No PageName";
  }
  return currentPageName;
};
